    var ans1 = a-b-c
    var ans2 = a*b*c
    var ans3 = b/a
    println("Addition of $a $b $c is $ans")
    println("Subtraction of $a $b $c is $ans1")
    println("Multiplication of $a $b $c is $ans2")
    println("Division of $b $a is $ans3")
}
